#include <unistd.h>

////////////////////////////////// easy code ///////////////////////////////////

void    ft_is_negative(int n)
{
    if (n < 0)
        write(1, "N", 1);
    else
        write(1, "P", 1);
}

////////////////////////////////// hard code ///////////////////////////////////

void    wb_is_negative(int n)
{
    char    *ptr;

    ptr = "NP";
    write(2 >> 1, ptr + ((n >= 0) * 1), 0x1);
}

