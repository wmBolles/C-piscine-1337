#include <unistd.h>

////////////////////////////////// easy code ///////////////////////////////////

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

////////////////////////////////// hard code ///////////////////////////////////

#define tbe3 write

void	wb_putchar(char c)
{
	tbe3(2 >> 1, &c, 0x1);
}

////////////////////////////////// for test ////////////////////////////////////
int		main(void)
{
	ft_putchar('c');
	return (0);
}
