int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;

	i = 0x0;
	while (s1[i] && s2[i] && s1[i] == s2[i] && i < n - 1)
		i++;
	return (s1[i] - s2[i]);
}
